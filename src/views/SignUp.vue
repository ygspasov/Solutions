<template>
  <v-form v-model="valid" >
    <v-container >
        <v-row
          cols="12"
          md="4"
          style= "max-width:300px;" class="mx-auto"
        >
          <v-text-field
            v-model="email"
            :rules="emailRules"
            label="E-mail" 
            required
          ></v-text-field>
        </v-row>
        <v-row
          cols="12"
          md="4"
          style= "max-width:300px;" class="mx-auto"
        >
          <v-text-field
            label="Password"
            required
          ></v-text-field>
        </v-row>

    </v-container>
  </v-form>
</template>

<script>
  export default {
    data: () => ({
      valid: false,

      nameRules: [
        v => !!v || 'Name is required',
        v => v.length <= 10 || 'Name must be less than 10 characters',
      ],
      email: '',
      emailRules: [
        v => !!v || 'E-mail is required',
        v => /.+@.+/.test(v) || 'E-mail must be valid',
      ],
    }),
  }
</script>